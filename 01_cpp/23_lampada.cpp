#include<iostream>

using namespace std;

int main(){
    int n=0, a=0, b=0;

    cin >> n;

    for(int i=0; i<n; i++){
        int t=0;

        cin >> t;

        if(t == 1){
            a == 0? a = 1 : a = 0;
        } else{
            a == 0? a = 1 : a = 0;
            b == 0? b = 1 : b = 0;
        }
    }

    cout << a << endl;
    cout << b << endl;

    return 0;
}